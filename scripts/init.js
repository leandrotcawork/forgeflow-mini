#!/usr/bin/env node

/**
 * ForgeFlow Mini — Brain Initialization Script (v2)
 * Rewritten to avoid security hook issues
 *
 * Usage: node scripts/init.js [project-path]
 */

const fs = require('fs');
const path = require('path');
const { promisify } = require('util');
const { execFileSync } = require('child_process');

const readdir = promisify(fs.readdir);
const stat = promisify(fs.stat);
const readFile = promisify(fs.readFile);
const writeFile = promisify(fs.writeFile);
const mkdir = promisify(fs.mkdir);

// Simple color output (no external deps)
const colors = {
  cyan: '\x1b[36m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  blue: '\x1b[34m',
  dim: '\x1b[2m',
  reset: '\x1b[0m',
};

function log(color, text) {
  console.log(`${color}${text}${colors.reset}`);
}

/**
 * Check if file exists
 */
function fileExists(filePath) {
  try {
    fs.accessSync(filePath, fs.constants.F_OK);
    return true;
  } catch {
    return false;
  }
}

/**
 * Read JSON file safely
 */
function readJSON(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf-8');
    return JSON.parse(content);
  } catch {
    return null;
  }
}

/**
 * Detect project type
 */
function detectProjectType(projectPath) {
  const detections = {
    hasPackageJson: fileExists(path.join(projectPath, 'package.json')),
    hasGoMod: fileExists(path.join(projectPath, 'go.mod')),
    hasPyproject: fileExists(path.join(projectPath, 'pyproject.toml')),
    hasRequirements: fileExists(path.join(projectPath, 'requirements.txt')),
    hasSrcComponents: fileExists(path.join(projectPath, 'src', 'components')),
    hasServerDir: fileExists(path.join(projectPath, 'server')) ||
                  fileExists(path.join(projectPath, 'api')) ||
                  fileExists(path.join(projectPath, 'apps', 'server_core')),
    hasMigrations: fileExists(path.join(projectPath, 'migrations')),
  };

  return detections;
}

